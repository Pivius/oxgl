pub enum Primitive {
	Quad,
	Triangle,
	Cube,
}

impl Primitive {
	pub fn vertices(&self) -> Vec<f32> {
		match self {
			Primitive::Quad => vec![
				-0.5, 0.5, 0.0, -0.5, -0.5, 0.0, 0.5, -0.5, 0.0,
				-0.5, 0.5, 0.0, 0.5, -0.5, 0.0, 0.5, 0.5, 0.0,
			],
			Primitive::Triangle => vec![
				0.0, 0.5, 0.0, -0.5, -0.5, 0.0, 0.5, -0.5, 0.0,
			],
			Primitive::Cube => vec![
				// Front face
				-0.5, -0.5,  0.5, 0.5, -0.5,  0.5, 0.5,  0.5,  0.5,
				-0.5, -0.5,  0.5, 0.5,  0.5,  0.5, -0.5,  0.5,  0.5,
				// Back face
				-0.5, -0.5, -0.5, -0.5,  0.5, -0.5, 0.5,  0.5, -0.5,
				-0.5, -0.5, -0.5, 0.5,  0.5, -0.5, 0.5, -0.5, -0.5,
				// Left face
				-0.5, -0.5, -0.5, -0.5, -0.5,  0.5, -0.5,  0.5,  0.5,
				-0.5, -0.5, -0.5, -0.5,  0.5,  0.5, -0.5,  0.5, -0.5,
				// Right face
				0.5, -0.5, -0.5, 0.5,  0.5, -0.5, 0.5,  0.5,  0.5,
				0.5, -0.5, -0.5, 0.5,  0.5,  0.5, 0.5, -0.5,  0.5,
				// Top face
				-0.5,  0.5, -0.5, -0.5,  0.5,  0.5, 0.5,  0.5,  0.5,
				-0.5,  0.5, -0.5, 0.5,  0.5,  0.5, 0.5,  0.5, -0.5,
				// Bottom face
				-0.5, -0.5, -0.5, 0.5, -0.5, -0.5, 0.5, -0.5,  0.5,
				-0.5, -0.5, -0.5, 0.5, -0.5,  0.5, -0.5, -0.5,  0.5,
			],
		}
	}
}